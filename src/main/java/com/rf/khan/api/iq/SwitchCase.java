package com.rf.khan.api.iq;

public class SwitchCase {
	public static void main(String[] args) {

		char ch = 'a';

		switch (ch) {
		case 1:
			System.out.println("case 1");
			break;
		case 'a':
			System.out.println("case 'a'");
			break;
		default:
			System.out.println("default case");
			break;
		}
	}

}
